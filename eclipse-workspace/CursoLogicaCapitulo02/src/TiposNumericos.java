
public class TiposNumericos {
	
	public static void main(String[] args) {
		Byte numerobyte = 1; // -128 até 127
		
		Short numeroShort = 1; // -32768 até32767
		
		Integer numeroInteger = 1; // -21474836488 até 2147483647
		
		Long numeroLong = 1L; // -9,223372037 x10 até 9,223372034x10
		
		Float numeroFloat = 1.0f; 
		
		Double numeroDouble = 1.0; 
	}

}
